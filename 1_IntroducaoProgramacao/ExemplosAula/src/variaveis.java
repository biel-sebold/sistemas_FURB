public class variaveis {
    public static void main(String[] args) {
        // TIPOS PRIMITIVOS: (8)
        //inteiros (4)
        int variavelInteira = 1;
        System.out.println(variavelInteira);

        byte variavelByte = 0;
        System.out.println(variavelByte);

        short variavelShort = 0;
        System.out.println(variavelShort);

        long variavelLong = 0;
        System.out.println(variavelLong);

        //reais
        double varivelDouble = 0;
        System.out.println(varivelDouble);

        float variavelFloat = 0;
        System.out.println(variavelFloat);

        //lógico (1)
        boolean variavelLogica = true;
        System.out.println(variavelLogica);

        //caracter (1)
        char variavelCaracter = 'A';
        System.out.println(variavelCaracter);


        // ISSO NÃO É UM TIPO PRIMITIVO
        //cadeia de caracteres
        String variavelCadeiaCaracteres = "Gabriel";
        System.out.println(variavelCadeiaCaracteres);



    }

}
